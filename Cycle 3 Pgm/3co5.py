import statistics as s
list1=[20,30,40,50,60]
print("Mean:",s.mean(list1))
print("Median:",s.median(list1))
print("Mode:",s.mode(list1))
print("Harmonic Mean:",s.harmonic_mean(list1))
print("Statistic Variance:",s.variance(list1))
print("Statistic Median Low:",s.median_low([-12,6.6,-3,4,6,7.1,-9,22]))